#include <iostream>
using namespace std;

int main(){

    int number1 = 18; // Decimal
    int number2 = 022; // Octal
    int number3 = 0x012; //hexa
    int number4 = 0b00010010; // binary 

    cout << "Number 1: " << number1 << endl;
    cout << "Number 2: " << number2 << endl;
    cout << "Number 3: " << number3 << endl;
    cout << "Number 4: " << number4;

    return 0;
}